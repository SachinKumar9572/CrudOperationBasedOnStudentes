package com.java;

public class Student1 
{
  private int id;
  private String name;
  private String sub;
  private String mail;
  
  
  
  
public Student1(int id, String name, String sub) 
{
	super();
	this.id = id;
	this.name = name;
	this.sub = sub;
	this.mail = mail;
}
public int getId() 
{
	return id;
}
public void setId(int id) 
{
	this.id = id;
}
public String getName() 
{
	return name;
}
public void setName(String name) 
{
	this.name = name;
}
public String getSub() 
{
	return sub;
}
public void setSub(String sub) 
{
	this.sub = sub;
}
public String getMail() 
{
	return mail;
}
public void setMail(String mail) 
{
	this.mail = mail;
}
}
